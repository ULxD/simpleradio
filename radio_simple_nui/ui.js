function pvd(){
	$("#mainmenu").css("display", "none");
	$("#menuspvd").css("display", "block");
}

function opens(){
	$("#mainmenu").css("display", "none");
	$("#menusopen").css("display", "block");
}

function reset(){
	$("#mainmenu").css("display", "block");
	$("#menusopen").css("display", "none");
	$("#menuspvd").css("display", "none");
}

